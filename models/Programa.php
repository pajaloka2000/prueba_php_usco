<?php

namespace Model;


class Programa extends ActiveRecord{
    protected static $tabla = 'programa_academico';
    protected static $columnasDB = ['id', 'nombre'];

    public $id;
    public $nombre;

    public function __construct($args = [])
    {
        $this->id = $args['id'] ?? null;
        $this->nombre = $args['nombre'] ?? '';
    }

}